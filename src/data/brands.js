export function generateSampleBrands(targetCount = 1000) {
  const results = [];
  let iteration = 0;
  while (results.length < targetCount) {
    BASE_BRANDS.forEach((base) => {
      if (results.length >= targetCount) return;
      const variantIndex = iteration === 0 ? "" : `（サンプル${iteration + 1}）`;
      results.push({
        id: `brand-${results.length}`,
        displayName: `${base.displayName}${variantIndex}`,
        latinName:
          iteration === 0 ? base.latinName : `${base.latinName} Sample ${iteration + 1}`,
        logoText: base.logoText
      });
    });
    iteration += 1;
  }
  return results;
}

export function shuffleArray(array) {
  const result = [...array];
  for (let i = result.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [result[i], result[j]] = [result[j], result[i]];
  }
  return result;
}

